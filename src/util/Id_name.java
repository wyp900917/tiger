package util;

public class Id_name {

	private String id;
	private int value;

	public Id_name(String id, int value) {
		this.id = id;
		this.value = value;
	}
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

}
